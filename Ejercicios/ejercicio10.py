# Crear un programa que pida al usuario 3 números e imprima por pantalla el promedio de dichos números.

num1 = int(input("ingrese primer nota: "))
num2 = int(input("ingrese segunda nota: "))
num3 = int(input("ingrese tercer nota "))

promedio = (num1 + num2 + num3)/3

print (f"el promedio de las 3 notas es de {promedio}")


