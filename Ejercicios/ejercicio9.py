# Crear un programa que pida al usuario una temperatura en grados Celsius e imprima por
#pantalla su equivalente en grados Fahrenheit

tempCelsius = int(input("ingrese temperatura en grados celsius: "))

fahrenheit = 9/5*tempCelsius + 32

print (f"la temperatura {tempCelsius} en grados celsius, convertida en grados fahrenheit serian {fahrenheit}grados")